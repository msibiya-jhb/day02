#include <unistd.h>

void ft_putchar(char c)
{
	write(1, &c, 1);
}

void ft_print_alphabet()
{
	char i;
		i = 'a';

	while(i >= 'a')
	{
		ft_putchar(i);
		i++;
	}
}
